export function openApiText(key: string, fallback = "", params: TranslateParams = {}) {
  const text = openApiMessages[currentLocale()]?.[key] || openApiMessages[DEFAULT_LOCALE]?.[key] || fallback;
  return interpolate(text, params);
}

function currentLocale(): Locale {
  const locale = i18n.global.locale.value;
  return isLocale(locale) ? locale : DEFAULT_LOCALE;
}

function interpolate(text: string, params: TranslateParams) {
  return text.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ""));
}
